'''
    각각의 특성과 장점
    1. float('inf'): 기본적인 무한대 표현, 모든 상황에서 사용 가능
    2. math.inf: 수학적 계산에 최적화, 수학 관련 함수와 함께 사용 시 유리
    3. numpy.inf: 배열 연산에 최적화, NumPy 배열과 함께 사용 시 유리
    4. pandas.NA: 결측치 표현, Pandas 데이터프레임에서 결측치 처리에 유리

    단점
    1. float('inf'): 메모리 사용량이 많고, 속도가 느릴 수 있음
    2. math.inf: 수학적 연산에 최적화되어 있지만, 일반적인 상황에서는 불편할 수 있음
    3. numpy.inf: NumPy 배열 전용, 다른 데이터 구조와의 호환성이 떨어질 수 있음
    4. pandas.NA: 결측치 표현에 특화되어 있어, 일반적인 무한대 표현에는 부적합
'''